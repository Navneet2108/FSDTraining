
public class containers {

	public static void main(String[] args) {
		System.out.println("This is statement 1");
		System.out.println("This is statement 2");
		System.out.println("This is statement 3");
		byte age=10;
		System.out.println("age is :"+age);
		age=12;
		System.out.println("age is :"+age);
		age=(byte)128;                                      //downcasting
		System.out.println("age now is :"+age);
		age=(byte)256;                                      //downcasting
		System.out.println("age now is :"+age);
		int num=10000;
		long salary=100000231210L;
		byte johnsAge=10;
		System.out.println(" john age now is :"+johnsAge);
		System.out.println("num is :"+num);
		System.out.println("salary is :"+salary);
		float pi=3.14f;
		double pi1=3.14;
		System.out.println("  pi is :"+pi+" && pi1 is :"+pi1);
	}

}
